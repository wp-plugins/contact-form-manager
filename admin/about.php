<h1  style="visibility:visible;">Contact Form Manager (V 1.0)</h1>
Create and manage multiple contact forms for your website. The contact form manager plugin supports a wide range of contact form elements such as text field, email field, textarea, dropdown list, radio button, checkbox, date picker, captcha, file uploader etc. Shortcodes are generated such that, you can modify form element properties without having to replace the shortcode everytime. 
Contact Form Manager is developed and maintained by <a href="http://xyzscripts.com">xyzscripts</a>. 

   
    <br />
<h2>Features</h2>
    <div >
      <p></p>
      
      
      <div >
      <ul>

    <li>Full control on Contact Form</li>
    <li>Multiple Contact Forms</li>
    <li>Support the form elements text, email, text area, dropdown, checkbox, radiobutton, date, captcha, file upload and submit button</li>
    <li>Auto save contact forms</li>
    <li>Multi language support</li>
    <li>Edit the contact form without modifying the short code</li>
    <li>Autoreply feature</li>
    <li>Redirection after submission feature</li>
    <li>Standard captcha and reCaptcha options</li>
    <li>Style class integration option with form elements</li>
    <li>Option to add * (star symbol) for mandatory fields</li>
    <li>Tiny MCE filter option</li>
    <li>Shortcodes for contact forms</li>
    <li>Visual HTML editor for beautiful contact forms</li>
