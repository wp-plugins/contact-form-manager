<h1 style="visibility: visible;">Contact Form Manager (V <?php echo xyz_plugin_get_version(); ?>)</h1>
Create and manage multiple contact forms for your website. The contact
form manager plugin supports a wide range of contact form elements such
as text field, email field, textarea, dropdown list, radio button,
checkbox, date picker, captcha, file uploader etc. Shortcodes are
generated such that, you can modify form element properties without
having to replace the shortcode everytime. Contact Form Manager is
developed and maintained by
<a href="http://xyzscripts.com">xyzscripts</a>
.


